namespace SPACalculator.Models;

public class JulianTimeModel
{
	/// <summary>
	///     Julian century
	/// </summary>
	public double JCentury;

	/// <summary>
	///     Julian ephemeris century
	/// </summary>
	public double JECentury;

	/// <summary>
	///     Julian day
	/// </summary>
	public double JDay;

	/// <summary>
	///     Julian ephemeris day
	/// </summary>
	public double JEDay;

	/// <summary>
	///     Julian ephemeris millennium
	/// </summary>
	public double JEMillennium;
}